#Ejemplo operadores Aritmeticos
#numero1:int=18
#numero2:int=12
numero1= int(input("Ingrese el numero1: "))
numero2= int(input("Ingrese el numero1: "))

suma = numero1 + numero2
resta = numero1 - numero2
division = numero1 / numero2
multiplicacion = numero1 * numero2
modulo = numero1 % numero2
divisionEntera = numero1 // numero2

print(f"La suma de {numero1} y {numero2} es {suma}")
print(f"La resta de {numero1} y {numero2} es {resta}")
print(f"La division de {numero1} y {numero2} es {division}")
print(f"La multiplicacion de {numero1} y {numero2} es {multiplicacion}")
print(f"La modulo de {numero1} y {numero2} es {modulo}")
print(f"La division entera de {numero1} y {numero2} es {divisionEntera}")



